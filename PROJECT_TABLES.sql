USE PROJECT;

create table PATIENT (
PATIENT_ID int primary key, 
PT_FNAME VARCHAR(20),
PT_LNAME VARCHAR(20),
PT_SEX VARCHAR(2),
PT_DOB DATETIME NOT NULL,
PT_WEIGHT INT,
PT_ETHNICITY VARCHAR(15)
);

